let n = prompt ('Укажите число от 0 до 59');

if (0 < n && n < 16) {
    alert ( 'Первая четверть часа');
} else if (15 < n && n < 31) {
    alert ( 'Вторая четверть часа');
} else if (30 < n && n < 46) {
    alert ( 'Третья четверть часа');
} else if (45 < n && n < 60) {
    alert ( 'Четвертая четверть часа');
} else {
    alert ( 'Неверно указано значение');
}